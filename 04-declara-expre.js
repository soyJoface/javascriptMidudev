/*Declaraciones
Las declaraciones en JavaScript son sentencias 
que definen la creación de una variable, función o clase. 
Podríamos decir que las declaraciones son como las instrucciones que le damos a JavaScript para que haga algo.

Por ejemplo, una declaración de variable es una sentencia que le da un nombre y un valor a una variable.
El siguiente código es un ejemplo de una declaración de variable:

let nombre = "Juan"

*/


/* Expresiones
Las expresiones en JavaScript son sentencias que producen un valor. 
Las expresiones pueden ser tan simples como un solo número o una cadena de texto, o tan complejas como el cálculo de una operación matemática,
 la evaluación de diferentes valores o la llamada a una función.

Por ejemplo, una expresión numérica es una sentencia que produce un número:
2 + 3 // -> 5

De hecho, lo que guardamos en las variables son expresiones. 
Por ejemplo, en el siguiente código, la expresión 2 + 3 se evalúa y el resultado se guarda en la variable resultado:

let resultado = 2 + 3

*/